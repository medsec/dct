static void print_hex(const char *message, 
                      const uint8_t *x, 
                      const size_t len)
{
    size_t i;
    puts(message);

    for (i = 0; i < len; i++) {
        if ((i != 0) && (i % 16 == 0)) {
            puts("");
        }

        printf("%02x ", x[i]);
    }

    printf("     %zu (octets)\n\n", len);
}

// ---------------------------------------------------------------------

static void print_context(const dct_context_t *ctx)
{
    print_hex("Hash Key: ", (uint8_t*)ctx->hash_ctx.k1, BLOCKLEN);
    print_hex("Hash Key: ", (uint8_t*)ctx->hash_ctx.k2, BLOCKLEN);
    print_hex("Simpira Key: ", (uint8_t*)&(ctx->simpira_key), BLOCKLEN);
    print_hex("Mode Key: ", (uint8_t*)ctx->expanded_key, DEOXYS_ROUND_KEYS * BLOCKLEN);
}

// ---------------------------------------------------------------------

static void test_output(const dct_context_t *ctx,
                        const uint8_t *k, const size_t klen,
                        const uint8_t *h, const size_t hlen,
                        const uint8_t *m, const size_t mlen,
                        const uint8_t *c, const size_t clen)

{
    print_hex("SK: ", k, klen);
    print_context(ctx);
    print_hex("Header: ", h, hlen);
    print_hex("Plaintext:", m, mlen);
    print_hex("Ciphertext:", c, clen);
    puts("\n\n");
}

// ---------------------------------------------------------------------

static void free_up(uint8_t* c, 
                    uint8_t* m, 
                    const size_t mlen)
{
    if (mlen != 0) {
        free(m);
        free(c);
    }
}

// ---------------------------------------------------------------------

static int run_test(const uint8_t *k,
                    const uint8_t *h, 
                    const size_t hlen,
                    const uint8_t *expected_m,
                    size_t mlen,
                    const uint8_t *expected_c)
{
    dct_context_t ctx;
    uint8_t* c = NULL;
    uint8_t* m = NULL;

    if (mlen != 0) {
        c = (uint8_t*)calloc((size_t)(mlen + TAGLEN), 1);
        m = (uint8_t*)calloc((size_t)mlen, 1);
    }

    size_t clen;
    keysetup(&ctx, k);
    encrypt_final(&ctx, c, &clen, h, hlen, expected_m, mlen);

    if (((mlen != 0) && memcmp(expected_c, c, clen))) {
        test_output(&ctx, k, KEYLEN, h, hlen, expected_m, mlen, c, clen);
        puts("Encryption produced incorrect result");
        free_up(c, m, mlen);
        return -1;
    }

    keysetup(&ctx, k);

    const int result = decrypt_final(&ctx, m, &mlen, h, hlen, c, clen);
    test_output(&ctx, k, KEYLEN, h, hlen, m, mlen, c, clen);

    if ((mlen != 0) && memcmp(expected_m, m, mlen)) {
        puts("Decryption produced incorrect result");
        free_up(c, m, mlen);
        return -1;
    }
    
    free_up(c, m, mlen);
    return result;
}

